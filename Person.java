/**
 * Represents a person who could be a driver or passenger of a car.
 * @author Souley
 *
 */
public class Person {
	
	/**
	 * Name of person.
	 */
	String name;
	
	/**
	 * Age of person.
	 */
	int age;
	
	/**
	 * Creates a person with given name and age.
	 * @param name of person
	 * @param age of person
	 */
	public Person(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
}
